package tankgame05;

public class Base {
    int x;
    int y;
    int life=2;
    int wide1;
    int length1;
    int wide2;
    int length2;

    public Base(int x, int y, int life, int wide1, int length1, int wide2, int length2) {
        this.x = x;
        this.y = y;
        this.life = life;
        this.wide1 = wide1;
        this.length1 = length1;
        this.wide2 = wide2;
        this.length2 = length2;
    }
}
